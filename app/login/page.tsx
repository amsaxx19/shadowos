"use client"

import { useState } from "react"
import React from "react"
import { sendLoginOtp } from "./actions"
import { Button } from "@/components/ui/button"
import { Input } from "@/components/ui/input"
import { Label } from "@/components/ui/label"
import { toast } from "sonner"
import { Loader2 } from "lucide-react"
import { useRouter } from "next/navigation"
import Link from "next/link"
import Image from "next/image"

export default function LoginPage() {
    return (
        <React.Suspense fallback={<div className="min-h-screen bg-[#0a0a0a] flex items-center justify-center"><Loader2 className="animate-spin text-white" /></div>}>
            <LoginForm />
        </React.Suspense>
    )
}

function LoginForm() {
    const router = useRouter()
    const [isLoading, setIsLoading] = useState(false)

    const handleSendOtp = async (e: React.FormEvent<HTMLFormElement>) => {
        e.preventDefault()
        setIsLoading(true)
        const formData = new FormData(e.currentTarget)
        const emailInput = formData.get('email') as string

        try {
            const result = await sendLoginOtp(emailInput)

            if (result.error) {
                toast.error(result.error)
            } else {
                toast.success("Code sent to your email!")
                router.push(`/signup/verify?email=${encodeURIComponent(emailInput)}`)
            }
        } catch (error) {
            toast.error("An unexpected error occurred")
            console.error(error)
        } finally {
            setIsLoading(false)
        }
    }

    return (
        <div className="min-h-screen bg-[#0a0a0a] flex flex-col lg:flex-row text-white font-sans">
            {/* Left Side - Illustration */}
            <div className="hidden lg:flex lg:w-1/2 xl:w-3/5 relative flex-col items-center justify-center p-12">
                {/* Hero Image */}
                <div className="relative w-full max-w-lg aspect-square mb-8">
                    <Image
                        src="/signup_hero.png"
                        alt="CUANBOSS Illustration"
                        fill
                        className="object-contain"
                        priority
                    />
                </div>

                {/* Tagline */}
                <h1 className="text-4xl xl:text-5xl font-bold text-center tracking-tight">
                    Where the internet does business
                </h1>
            </div>

            {/* Right Side - Auth Form */}
            <div className="flex-1 flex items-center justify-center p-6 lg:p-12">
                <div className="w-full max-w-[400px]">
                    {/* Form Card */}
                    <div className="bg-[#141414] border border-[#2a2a2a] rounded-2xl p-8 shadow-xl">
                        {/* Logo */}
                        <div className="flex justify-center mb-6">
                            <div className="flex items-center gap-2 text-xl font-bold">
                                <span className="text-orange-500 text-2xl">⚡</span>
                                <span className="text-white">CUANBOSS</span>
                            </div>
                        </div>

                        {/* Description */}
                        <p className="text-neutral-400 text-center text-sm mb-8">
                            Welcome back! Please enter your details.
                        </p>

                        {/* Email Form */}
                        <form onSubmit={handleSendOtp} className="space-y-4">
                            <div className="space-y-2">
                                <Label htmlFor="email" className="text-neutral-400 text-sm">Email</Label>
                                <Input
                                    id="email"
                                    name="email"
                                    type="email"
                                    placeholder="johnappleseed@gmail.com"
                                    required
                                    className="bg-[#0a0a0a] border-[#333] text-white placeholder:text-neutral-600 focus:border-blue-500 focus:ring-1 focus:ring-blue-500 h-12 rounded-xl text-base px-4"
                                />
                            </div>

                            <Button
                                type="submit"
                                disabled={isLoading}
                                className="w-full bg-blue-600 hover:bg-blue-700 text-white font-semibold h-12 rounded-xl text-base transition-colors"
                            >
                                {isLoading ? <Loader2 className="animate-spin" /> : "Continue"}
                            </Button>
                        </form>

                        {/* Divider */}
                        <div className="relative my-6">
                            <div className="absolute inset-0 flex items-center">
                                <span className="w-full border-t border-[#2a2a2a]" />
                            </div>
                            <div className="relative flex justify-center text-xs uppercase">
                                <span className="bg-[#141414] px-3 text-neutral-500">OR</span>
                            </div>
                        </div>

                        {/* Social Logins */}
                        <div className="grid grid-cols-3 gap-3">
                            <Button
                                variant="outline"
                                className="bg-white hover:bg-neutral-100 text-black border-none h-11 rounded-xl"
                                onClick={() => toast.info("Google Login Coming Soon!")}
                            >
                                <svg className="h-5 w-5" viewBox="0 0 24 24">
                                    <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" fill="#4285F4" />
                                    <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853" />
                                    <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" fill="#FBBC05" />
                                    <path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335" />
                                </svg>
                            </Button>
                            <Button
                                variant="outline"
                                className="bg-white hover:bg-neutral-100 text-black border-none h-11 rounded-xl"
                                onClick={() => toast.info("Apple Login Coming Soon!")}
                            >
                                <svg className="h-5 w-5 fill-current" viewBox="0 0 24 24">
                                    <path d="M17.05 20.28c-.98.95-2.05.8-3.08.35-1.09-.46-2.09-.48-3.24 0-1.44.62-2.2.44-3.06-.35C2.79 15.25 3.51 7.59 9.05 7.31c1.35.07 2.29.74 3.08.74s2.57-.99 3.87-.75c1.29.24 2.23.85 2.84 1.71-2.68 1.57-2.16 5.21.41 6.12-.71 1.97-1.75 3.95-2.2 5.15zM12.03 7.25c-.15-2.23 1.66-4.07 3.74-4.25.29 2.58-2.34 4.5-3.74 4.25z" />
                                </svg>
                            </Button>
                            <Button
                                variant="outline"
                                className="bg-[#5865F2] hover:bg-[#4752C4] text-white border-none h-11 rounded-xl"
                                onClick={() => toast.info("Discord Login Coming Soon!")}
                            >
                                <svg className="h-5 w-5 fill-current" viewBox="0 0 24 24">
                                    <path d="M20.317 4.37a19.791 19.791 0 0 0-4.885-1.515.074.074 0 0 0-.079.037c-.21.375-.444.864-.608 1.25a18.27 18.27 0 0 0-5.487 0 12.64 12.64 0 0 0-.617-1.25.077.077 0 0 0-.079-.037A19.736 19.736 0 0 0 3.677 4.37a.07.07 0 0 0-.032.027C.533 9.046-.32 13.58.099 18.057a.082.082 0 0 0 .031.057 19.9 19.9 0 0 0 5.993 3.03.078.078 0 0 0 .084-.028c.462-.63.874-1.295 1.226-1.994a.076.076 0 0 0-.041-.106 13.107 13.107 0 0 1-1.872-.892.077.077 0 0 1-.008-.128 10.2 10.2 0 0 0 .372-.292.074.074 0 0 1 .077-.01c3.928 1.793 8.18 1.793 12.062 0a.074.074 0 0 1 .078.01c.12.098.246.198.373.292a.077.077 0 0 1-.006.127 12.299 12.299 0 0 1-1.873.892.077.077 0 0 0-.041.107c.36.698.772 1.362 1.225 1.993a.076.076 0 0 0 .084.028 19.839 19.839 0 0 0 6.002-3.03.077.077 0 0 0 .032-.054c.5-5.177-.838-9.674-3.549-13.66a.061.061 0 0 0-.031-.03zM8.02 15.33c-1.183 0-2.157-1.085-2.157-2.419 0-1.333.956-2.419 2.157-2.419 1.21 0 2.176 1.086 2.157 2.419 0 1.334-.956 2.42-2.157 2.42zm7.975 0c-1.183 0-2.157-1.085-2.157-2.419 0-1.333.955-2.419 2.157-2.419 1.21 0 2.176 1.086 2.157 2.419 0 1.334-.946 2.42-2.157 2.42z" />
                                </svg>
                            </Button>
                        </div>

                        {/* Signup Link */}
                        <div className="text-center text-sm text-neutral-400 mt-6">
                            Don't have an account? <Link href="/signup" className="text-blue-500 hover:underline font-medium">Sign up</Link>
                        </div>

                        {/* Terms */}
                        <div className="text-center text-[11px] text-neutral-500 mt-4">
                            By signing in you agree to CUANBOSS's{" "}
                            <Link href="#" className="underline hover:text-neutral-400">Terms of Service</Link> and{" "}
                            <Link href="#" className="underline hover:text-neutral-400">Privacy Policy</Link>.
                        </div>
                    </div>
                </div>
            </div>

            {/* Mobile Logo - shown only on smaller screens */}
            <div className="lg:hidden absolute top-6 left-1/2 -translate-x-1/2">
                <span className="text-orange-500 text-3xl">⚡</span>
            </div>
        </div>
    )
}
